# –ê–Ω–∞–ª–æ–≥–∏—á–Ω–∞ –Ω–∞ –≥–æ—Ä–Ω–∞—Ç–∞, –ø—Ä–æ—Å—Ç–æ —Å–µ–≥–∞ —Å–º–µ —Ä–∞–∑–º–µ–Ω–∏–ª–∏ –æ—á–∞–∫–≤–∞–Ω–µ—Ç–æ –¥–∞ –µ —Ä–∞–∑–ª–∏—á–Ω–æ, –∞ –Ω–µ –¥–∏—Å–ø–µ—Ä—Å–∏—è—Ç–∞, –∫–∞–∫—Ç–æ –∏ –∏–º–∞–º–µ –æ—â–µ 1 —Å—Ç–æ–π–Ω–æ—Å—Ç –∑–∞ n=1000
