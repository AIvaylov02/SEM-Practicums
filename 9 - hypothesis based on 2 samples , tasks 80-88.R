sim_t_test_2_samples <- function(n, mu1, mu2, sd1, sd2) {
  x <- rnorm(n, mu1, sd1)
  y <- rnorm(n, mu2, sd2)
  # Ho: mu_x = mu_y
  # H1: mu_x != mu_y
  t.test(x, y)$p.value # –ø–æ-–ø–æ–¥—Ä–∞–∑–±–∏—Ä–∞–Ω–µ —Ä–∞–±–æ—Ç–∏ —Å two.sided –∏ –Ω–µ–∑–∞–≤–∏—Å–∏–º–∏ –∏–∑–≤–∞–¥–∫–∏
}

prob_t_test_2_samples <- function(trials, n_vector, acceptance_criteria, mu1, mu2, sd1, sd2) {
  n_len <- length(n_vector)
  percentage_vector <- numeric(length = n_len) # –ø–æ-–±—ä—Ä–∑–æ –µ –æ—Ç–∫–æ–ª–∫–æ—Ç–æ –¥–∞ –ø—Ä–∏–ª–µ–ø—è–º–µ —Å—Ç–æ–π–Ω–æ—Å—Ç—Ç–∞ –∫—ä–º –Ω–æ–≤ –≤–µ–∫—Ç–æ—Ä
  index <- 1
  while (index <= n_len) {
    n <- n_vector[index]
    rs <- replicate(trials, sim_t_test_2_samples(n, mu1, mu2, sd1, sd2))
    percentage_vector[index] <- sum(rs <= acceptance_criteria) / trials # –Ω–∞ –≤—Å–µ–∫–∏ –∏–Ω–¥–µ–∫—Å –∑–∞ n=i, —â–µ –∫–∞–∂–µ –≤ –∫–æ–ª–∫–æ % Ho —Å–µ –µ –æ—Ç—Ö–≤—ä—Ä–ª–∏–ª–∞
    index <- index + 1
  }
  percentage_vector
}

mu <- 5
n_vector <- c(20, 50, 100, 500)
prob_t_test_2_samples(trials = 10000, n_vector, acceptance_criteria = 0.05, mu1 = mu, mu2 = mu, sd1 = 1, sd2 = 0.8)
