# –≤) –ê–∫–æ –ø–µ—Ä–∞–ª–Ω—è –µ —Ä–∞–±–æ—Ç–∏–ª–∞ –±–µ–∑ –ø–æ–≤—Ä–µ–¥–∞ 3 –≥–æ–¥–∏–Ω–∏, –∫–∞–∫–≤–∞ –µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—Ç–∞ –¥–∞ —Ä–∞–±–æ—Ç–∏ –±–µ–∑ –ø–æ–≤—Ä–µ–¥–∞ –æ—â–µ 3 –≥–æ–¥–∏–Ω–∏?
# 3 –≥–æ–¥–∏–Ω–∏ –±–µ–∑ –ø–æ–≤—Ä–µ–¥–∞ –∏ –æ—â–µ 3 –≥–æ–¥–∏–Ω–∏ –±–µ–∑ –ø–æ–≤—Ä–µ–¥–∞ —Å–∞ 6 –≥–æ–¥–∏–Ω–∏ –±–µ–∑ –ø–æ–≤—Ä–µ–¥–∞ (–±–µ–∑–ø–∞–º–µ—Ç–Ω–æ—Å—Ç! -> –Ω–µ —Å–µ –∏–Ω—Ç–µ—Ä–µ—Å—É–≤–∞ –∫–∞–∫ –µ —Ä–∞–±–æ—Ç–∏–ª–∞ –ø—Ä–µ–∑ –º–∏–Ω–∞–ª–∏—Ç–µ 3 –≥–æ–¥–∏–Ω–∏)
# P(X > 6) = 1 - P(X <= 6)
1 - pexp(6, lambda)

# –≥) –ù–∞–º–µ—Ä–µ—Ç–µ t, –∑–∞ –∫–æ–µ—Ç–æ P(X ‚â§ t) = 0.90
# –°–µ–≥–∞ –Ω—è–º–∞ –¥–∞ —Å–µ –ø–∏—Ç–∞–º–µ –∑–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç, –∞ –∑–∞ –≤—Ä–µ–º–µ, —Ç–æ–µ—Å—Ç –∫–≤–∞–Ω—Ç–∏–ª–Ω–∞ —Ñ—É–Ω–∫—Ü–∏—è, –∫–∞—Ç–æ –∏—Å–∫–∞–º–µ –¥–∞ —Å–º–µ –ø–æ-–º–∞–ª–∫–∏ —Å –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç 0.9 => –Ω–µ –µ 1-p (–∑–∞ –ø–æ-–≥–æ–ª–µ–º–∏), –∞ —Å–∏ –µ p –≤ –∫–≤–∞–Ω—Ç–∏–ª–∞
qexp(0.9, lambda)



#–ó–∞–¥–∞—á–∞ 43. –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ—Ç–æ (–≤ –∫–≥) –∫–∞—à–∫–∞–≤–∞–ª, –∫–æ–µ—Ç–æ —Å–µ –∏–∑—Ä–∞–∑—Ö–æ–¥–≤–∞ –≤ –¥–∞–¥–µ–Ω–∞ –ø–∏—Ü–∞—Ä–∏—è –∑–∞ –µ–¥–Ω–∞ —Å–µ–¥–º–∏—Ü–∞, 
#            –µ –Ω–æ—Ä–º–∞–ª–Ω–æ —Ä–∞–∑–ø—Ä–µ–¥–µ–ª–µ–Ω–æ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∏ mu=41, sd=5.
mean <- 41
sd <- 5
#  –∞) –ö–∞–∫–≤–∞ –µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—Ç–∞ –≤ –ø—Ä–æ–∏–∑–≤–æ–ª–Ω–æ –∏–∑–±—Ä–∞–Ω–∞ —Å–µ–¥–º–∏—Ü–∞ –ø–∏—Ü–∞—Ä–∏—è—Ç–∞ –¥–∞ –∏–∑—Ä–∞–∑—Ö–æ–¥–∏ –Ω–∞–¥ 51–∫–≥ –∫–∞—à–∫–∞–≤–∞–ª?
# P(X > 51) = 1 - P(X <= 51)
1 - pnorm(51, mean, sd)

#  –±) –ö–∞–∫–≤–∞ –µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—Ç–∞ –≤ –ø—Ä–æ–∏–∑–≤–æ–ª–Ω–æ –∏–∑–±—Ä–∞–Ω–∞ —Å–µ–¥–º–∏—Ü–∞ –ø–∏—Ü–∞—Ä–∏—è—Ç–∞ –¥–∞ –∏–∑—Ä–∞–∑—Ö–æ–¥–∏ –º–µ–∂–¥—É 45 –∏ 50 –∫–≥ –∫–∞—à–∫–∞–≤–∞–ª?
# !!!!! –í–Ω–∏–º–∞–Ω–∏–µ –∑–∞ –≥—Ä–∞–Ω–∏—Ü–∏—Ç–µ!
# P(45 <= X <= 50) = P(X <= 50) - P(X <= 45), !!! –ø–∏—à–µ–º –≥–æ 45 –≤ –Ω–µ–ø—Ä–µ–∫—ä—Å–Ω–∞—Ç–∏ —Ä–∞–∑–ø—Ä–µ–¥–µ–ª–Ω–∏—è, –ø–æ–Ω–µ–∂–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—Ç–∞ –∑–∞ –ø–æ–ø–∞–¥–∞–Ω–µ –≤ —Ç–æ—á–∫–∞ –µ -> 0
# –ó–∞ —Ü–µ–ª–∏—Ç–µ –Ω–∏ —Ç–æ–≤–∞ –µ —Å—ä—â–æ—Ç–æ –∫–∞—Ç–æ P(X < 50) - P(X < 45), –≤—Å–µ —Ç–∞—è –µ –¥–∞–ª–∏ –ø–∏—à–µ–º —Ä–∞–≤–Ω–æ, –≤–∞–∂–Ω–æ –µ –¥–æ–ª–Ω–∞—Ç–∞ –≥—Ä–∞–Ω–∏—Ü–∞ –¥–∞ –µ 45, –∞ –Ω–µ 44, –∫–∞–∫—Ç–æ –±–∏—Ö–º–µ –Ω–∞–ø—Ä–∞–≤–∏–ª–∏ –≤ –¥–∏—Å–∫—Ä–µ—Ç–Ω–∏—Ç–µ —Ä–∞–∑–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è
pnorm(50, mean, sd) - pnorm(45, mean, sd)

#  –≤) –ö–æ–ª–∫–æ –∫–∞—à–∫–∞–≤–∞–ª —Ç—Ä—è–±–≤–∞ –¥–∞ –∏–º–∞ –≤ –∑–∞–ø–∞—Å –≤ –¥–∞–¥–µ–Ω–∞ —Å–µ–¥–º–∏—Ü–∞, —Ç–∞–∫–∞ —á–µ –¥–∞ –µ –¥–æ—Å—Ç–∞—Ç—ä—á–Ω–æ —Å –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç 0.99?
qnorm(0.99, mean, sd) # 0.99, –∞ –Ω–µ 0.01 –ø–æ–Ω–µ–∂–µ —Å–µ –ø–∏—Ç–∞–º–µ –∫–æ–ª–∫–æ –∫–∞—à–∫–∞–≤–∞–ª –¥–∞ –≤–∑–µ–º–µ–º, —Ç–∞–∫–∞ —á–µ –∏–∑—Ä–∞–∑—Ö–æ–¥–≤–∞–Ω–æ—Ç–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ (—Å–ª—É—á–∞–π–Ω–∞—Ç–∞ –≤–µ–ª–∏—á–∏–Ω–∞) –¥–∞ –µ –ø–æ-–º–∞–ª–∫–∞ –æ—Ç –Ω–µ–≥–æ —Å P=0.99


